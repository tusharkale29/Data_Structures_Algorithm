// Question : To find positive and negative number in given array : 

#include<iostream>
using namespace std;

int main()
{



    int num;
     cout << "Enter the number to check " << endl;
     cin >> num;

     if (num > 0)
     {
         cout <<  "Number is Positive number " << endl;
    }
     else
    {
         cout << "Number is Negative" << endl;
     }

    return 0;

}